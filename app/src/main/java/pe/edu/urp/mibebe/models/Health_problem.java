package pe.edu.urp.mibebe.models;


public class Health_problem {

    private int id;
    private String name;

    public Health_problem(int id, String name) {
        this.id = id;
        this.name = name;
    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
